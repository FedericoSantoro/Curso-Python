# Listas
lista = ["hola", True, 2, 3.267]
print("\nLista:")
print(lista)
print(lista[2])

# Tuplas ( No se puede modificar su contenido )
tupla = ("hola", True, 2, 3.267)
print("\nTuplas:")
print(tupla)
print(tupla[1])

# Set ( Elementos desordenados y sin repetir, no se puede modificar)
conjunto = {"hola", True, 2, 3.267, "hola"}
print("\nConjuntos:")
print(conjunto)

# Diccionario
diccionario = {
    "nombre" : "Fede",
    "apellido" : "Santoro",
    "edad" : 21,
    "altura" : 1.62,
    "nombre_repetido" : "Fede"
}
print("\nDiccionarios:")
print(diccionario)
print(diccionario["apellido"])